"""
字面量：就是直接写在代码中的数据
"""

# 字面量
# 数字类型：整数、浮点数   10  15.5
# 字符串类型：字符串 ‘hello’ ‘world’
# 布尔类型：True、False
# 空值：None
# 数据容器：列表、元组、字典、集合


print(100)  # 整数 int
print(15.5)  # 浮点数/小数 float
print(True)  # 布尔类型 bool
print(False)  # 布尔类型 bool
print("Hello World")  # 字符串 str
print(None)  # 空值 None

# 布尔类型本质也是整数类型，True对应1，False对应0
print(True + 1)  # 2
print(False - 1)  # -1
