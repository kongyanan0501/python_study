# """
# 案例2:计算输入的三个整数的平均数
# """

a = int(input("请输入第一个数字："))
b = int(input("请输入第二个数字："))
c = int(input("请输入第三个数字："))

avg = (a + b + c) / 3

print(f"{a},{b},{c} 这三个数的平均数为：{avg}")


# """
# 要求输入梯形的上底、下底和高，计算梯形的面积
# """

top = float(input("请输出梯形的上底："))
bottom = float(input("请输入梯形的下底："))
