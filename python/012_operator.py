# 算数运算符: + - * / % ** //
print("10 + 20 = ", 10 + 20)
print("10 - 20 = ", 10 - 20)
print("10 * 20 = ", 10 * 20)
print("10 / 20 = ", 10 / 20)
print("10 % 20 = ", 10 % 20)
print("10 ** 20 = ", 10**20)
print("10 // 20 = ", 10 // 20)

# 算数运算符的优先级: 先乘除后加减，先括号后乘除，先幂运算后乘除，先加减后移位，先关系运算后逻辑运算，先赋值后逗号
print("0.1 + 10 / 4 ** 2 = ", 0.1 + 10 / 4**2)


